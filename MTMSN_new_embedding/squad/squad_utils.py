def read_squad_examples(input_file, is_training, logger, debug=False):
    """Read a SQuAD json file into a list of SquadExample."""
    with open(input_file, "r") as reader:
        input_data = json.load(reader)["data"]

    def is_whitespace(c):
        if c == " " or c == "\t" or c == "\r" or c == "\n" or ord(c) == 0x202F:
            return True
        return False

    examples = []
    for entry in input_data:
        for paragraph in entry["paragraphs"]:
            paragraph_text = paragraph["context"]
            doc_tokens = []
            char_to_word_offset = []
            prev_is_whitespace = True
            for c in paragraph_text:
                if is_whitespace(c):
                    prev_is_whitespace = True
                else:
                    if prev_is_whitespace:
                        doc_tokens.append(c)    # add a new word
                    else:
                        doc_tokens[-1] += c     # add a new character
                    prev_is_whitespace = False
                char_to_word_offset.append(len(doc_tokens) - 1)

            for qa in paragraph["qas"]:
                qas_id = qa["id"]
                question_text = qa["question"]
                start_position = None
                end_position = None
                orig_answer_text = None
                if is_training:
                    if len(qa["answers"]) != 1:
                        raise ValueError(
                            "For training, each question should have exactly 1 answer.")
                    answer = qa["answers"][0]
                    orig_answer_text = answer["text"]
                    answer_offset = answer["answer_start"]
                    answer_length = len(orig_answer_text)
                    start_position = char_to_word_offset[answer_offset]
                    end_position = char_to_word_offset[answer_offset + answer_length - 1]
                    # Only add answers where the text can be exactly recovered from the
                    # document. If this CAN'T happen it's likely due to weird Unicode
                    # stuff so we will just skip the example.
                    #
                    # Note that this means for training mode, every example is NOT
                    # guaranteed to be preserved.
                    actual_text = " ".join(doc_tokens[start_position:(end_position + 1)])
                    cleaned_answer_text = " ".join(
                        tokenization.whitespace_tokenize(orig_answer_text))
                    if actual_text.find(cleaned_answer_text) == -1:
                        logger.warning("Could not find answer: '%s' vs. '%s'",
                                           actual_text, cleaned_answer_text)
                        continue

                example = SquadExample(
                    qas_id=qas_id,
                    question_text=question_text,
                    doc_tokens=doc_tokens,
                    orig_answer_text=orig_answer_text,
                    start_position=start_position,
                    end_position=end_position)
                examples.append(example)
            if debug:
                break
    return examples


def convert_examples_to_features(examples, tokenizer, max_seq_length, doc_stride,
                                 max_query_length, is_training,
                                 do_lm_training=False, lm_mask_prob=0,
                                 verbose_logging=False, logger=None):
    """Loads a data file into a list of `InputBatch`s."""

    unique_id = 1000000000

    features = []
    for (example_index, example) in enumerate(examples):
        query_tokens = tokenizer.tokenize(example.question_text)

        if len(query_tokens) > max_query_length:
            query_tokens = query_tokens[0:max_query_length]

        tok_to_orig_index = []
        orig_to_tok_index = []
        all_doc_tokens = []
        for (i, token) in enumerate(example.doc_tokens):
            orig_to_tok_index.append(len(all_doc_tokens))
            sub_tokens = tokenizer.tokenize(token)
            for sub_token in sub_tokens:
                tok_to_orig_index.append(i)
                all_doc_tokens.append(sub_token)

        tok_start_position = None
        tok_end_position = None
        if is_training:
            tok_start_position = orig_to_tok_index[example.start_position]
            if example.end_position < len(example.doc_tokens) - 1:
                tok_end_position = orig_to_tok_index[example.end_position + 1] - 1
            else:
                tok_end_position = len(all_doc_tokens) - 1
            (tok_start_position, tok_end_position) = _improve_answer_span(
                all_doc_tokens, tok_start_position, tok_end_position, tokenizer,
                example.orig_answer_text)

        # The -3 accounts for [CLS], [SEP] and [SEP]
        max_tokens_for_doc = max_seq_length - len(query_tokens) - 3

        # We can have documents that are longer than the maximum sequence length.
        # To deal with this we do a sliding window approach, where we take chunks
        # of the up to our max length with a stride of `doc_stride`.
        _DocSpan = collections.namedtuple(  # pylint: disable=invalid-name
            "DocSpan", ["start", "length"])
        doc_spans = []
        start_offset = 0
        while start_offset < len(all_doc_tokens):
            length = len(all_doc_tokens) - start_offset
            if length > max_tokens_for_doc:
                length = max_tokens_for_doc
            doc_spans.append(_DocSpan(start=start_offset, length=length))
            if start_offset + length == len(all_doc_tokens):
                break
            start_offset += min(length, doc_stride)

        for (doc_span_index, doc_span) in enumerate(doc_spans):
            sub_doc_tokens = all_doc_tokens[doc_span.start:(doc_span.start+doc_span.length)]

            token_positions = []
            tokens = []
            token_to_orig_map = {}
            segment_ids = []
            em_mask = []
            tokens.append("[CLS]")
            segment_ids.append(0)
            em_mask.append(0)
            for token in query_tokens:
                token_positions.append(len(tokens))
                tokens.append(token)
                segment_ids.append(0)
                if token in sub_doc_tokens and token not in stop_words:
                    em_mask.append(1)
                else:
                    em_mask.append(0)
            tokens.append("[SEP]")
            segment_ids.append(0)
            em_mask.append(0)

            for i in range(doc_span.length):
                split_token_index = doc_span.start + i
                token_to_orig_map[len(tokens)] = tok_to_orig_index[split_token_index]
                token_positions.append(len(tokens))
                token = all_doc_tokens[split_token_index]
                tokens.append(token)
                segment_ids.append(1)
                if token in query_tokens and token not in stop_words:
                    em_mask.append(1)
                else:
                    em_mask.append(0)
            tokens.append("[SEP]")
            segment_ids.append(1)
            em_mask.append(0)

            input_ids = tokenizer.convert_tokens_to_ids(tokens)

            # The mask has 1 for real tokens and 0 for padding tokens. Only real
            # tokens are attended to.
            input_mask = [1] * len(input_ids)

            # Zero-pad up to the sequence length.
            while len(input_ids) < max_seq_length:
                input_ids.append(0)
                input_mask.append(0)
                segment_ids.append(0)
                em_mask.append(0)

            assert len(input_ids) == max_seq_length
            assert len(input_mask) == max_seq_length
            assert len(segment_ids) == max_seq_length
            assert len(em_mask) == max_seq_length

            start_position = None
            end_position = None
            if is_training:
                # For training, if our document chunk does not contain an annotation
                # we throw it out, since there is nothing to predict.
                doc_start = doc_span.start
                doc_end = doc_span.start + doc_span.length - 1
                out_of_span = False
                if not (tok_start_position >= doc_start and tok_end_position <= doc_end):
                    out_of_span = True

                if out_of_span:
                    start_position = 0
                    end_position = 0
                else:
                    doc_offset = len(query_tokens) + 2
                    start_position = tok_start_position - doc_start + doc_offset
                    end_position = tok_end_position - doc_start + doc_offset

            masked_lm_positions = None
            masked_lm_weights = None
            masked_lm_ids = None
            masked_lm_input_ids = None
            mask_id = tokenizer.convert_tokens_to_ids(["[MASK]"])[0]
            if do_lm_training:
                max_num_masks = int(max_seq_length * lm_mask_prob)
                num_masks = int(len(token_positions) * lm_mask_prob) + 1
                num_masks = min(max_num_masks, num_masks)
                rng = random.Random()
                rng.shuffle(token_positions)
                masked_lm_positions = sorted(token_positions[:num_masks])
                masked_lm_input_ids = [x for x in input_ids]
                for m_pos in masked_lm_positions:
                    masked_lm_input_ids[m_pos] = mask_id
                    # prob = random.random()
                    # if prob < 0.8:
                    #     mask_id = tokenizer.convert_tokens_to_ids(["[MASK]"])[0]
                    #     masked_lm_input_ids[m_pos] = mask_id
                    # elif prob < 0.9:
                    #     mask_id = random.choice(range(len(tokenizer.vocab)))
                    #     masked_lm_input_ids[m_pos] = mask_id

                masked_lm_ids = [input_ids[m_pos] for m_pos in masked_lm_positions]
                masked_lm_weights = [1.0] * len(masked_lm_positions)
                if len(masked_lm_positions) < max_num_masks:
                    num_pads = max_num_masks - len(masked_lm_positions)
                    masked_lm_positions += [0] * num_pads
                    masked_lm_weights += [0.0] * num_pads
                    masked_lm_ids += [0] * num_pads

                assert len(masked_lm_input_ids) == max_seq_length
                assert len(masked_lm_positions) == max_num_masks
                assert len(masked_lm_weights) == max_num_masks
                assert len(masked_lm_ids) == max_num_masks

            if example_index < 2 and verbose_logging:
                logger.info("*** Example ***")
                logger.info("unique_id: %s" % (unique_id))
                logger.info("example_index: %s" % (example_index))
                logger.info("doc_span_index: %s" % (doc_span_index))
                logger.info("tokens: %s" % " ".join(
                    [tokenization.printable_text(x) for x in tokens]))
                logger.info("token_to_orig_map: %s" % " ".join(
                    ["%d:%d" % (x, y) for (x, y) in six.iteritems(token_to_orig_map)]))
                logger.info("input_ids: %s" % " ".join([str(x) for x in input_ids]))
                logger.info(
                    "input_mask: %s" % " ".join([str(x) for x in input_mask]))
                logger.info(
                    "segment_ids: %s" % " ".join([str(x) for x in segment_ids]))
                if is_training:
                    answer_text = " ".join(tokens[start_position:(end_position + 1)])
                    logger.info("start_position: %d" % (start_position))
                    logger.info("end_position: %d" % (end_position))
                    logger.info("answer: %s" % (tokenization.printable_text(answer_text)))

            feature = InputFeatures(
                    unique_id=unique_id,
                    example_index=example_index,
                    doc_span_index=doc_span_index,
                    tokens=tokens,
                    token_to_orig_map=token_to_orig_map,
                    input_ids=masked_lm_input_ids if do_lm_training and is_training else input_ids,
                    input_mask=input_mask,
                    em_mask=em_mask,
                    query_length=len(query_tokens) + 2,
                    segment_ids=segment_ids,
                    start_position=start_position,
                    end_position=end_position,
                    masked_lm_ids=masked_lm_ids,
                    masked_lm_weights=masked_lm_weights,
                    masked_lm_positions=masked_lm_positions
                )
            features.append(feature)
            unique_id += 1

            if len(features) % 5000 == 0:
                logger.info("Processing features: %d" % (len(features)))

    return features

def _improve_answer_span(doc_tokens, input_start, input_end, tokenizer,
                         orig_answer_text):
    """Returns tokenized answer spans that better match the annotated answer."""

    # The SQuAD annotations are character based. We first project them to
    # whitespace-tokenized words. But then after WordPiece tokenization, we can
    # often find a "better match". For example:
    #
    #   Question: What year was John Smith born?
    #   Context: The leader was John Smith (1895-1943).
    #   Answer: 1895
    #
    # The original whitespace-tokenized answer will be "(1895-1943).". However
    # after tokenization, our tokens will be "( 1895 - 1943 ) .". So we can match
    # the exact answer, 1895.
    #
    # However, this is not always possible. Consider the following:
    #
    #   Question: What country is the top exporter of electornics?
    #   Context: The Japanese electronics industry is the lagest in the world.
    #   Answer: Japan
    #
    # In this case, the annotator chose "Japan" as a character sub-span of
    # the word "Japanese". Since our WordPiece tokenizer does not split
    # "Japanese", we just use "Japanese" as the annotation. This is fairly rare
    # in SQuAD, but does happen.
    tok_answer_text = " ".join(tokenizer.tokenize(orig_answer_text))

    for new_start in range(input_start, input_end + 1):
        for new_end in range(input_end, new_start - 1, -1):
            text_span = " ".join(doc_tokens[new_start:(new_end + 1)])
            if text_span == tok_answer_text:
                return (new_start, new_end)

    return (input_start, input_end)


